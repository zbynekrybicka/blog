        <h2>Programování</h2>

        <h3>Petr &ndash; Gemtree (Pokud bych dnes začínal, daleko více a důsledněji bych plánoval)</h3>

        <p class="perex">Každý nějak začínal a každý se musel od něčeho odpíchnout. Já jsem začínal na vizuálním programovacím nástroji 
            <a href="http://www.gemtree.cz" target="_blank">Petr</a> od společnosti Gemtree.</p>

        <hr>

        <p>Pokud jste v programování naprostí zelenáči, ale vidíte v něm určitý hlubší smysl, rozhodně se vyplatí dát tomuto
            vizuálnímu nástroji šanci. Přestože se čistě jen s ním asi nedostanete mezi programátorskou elitu, rozhodně má
            smysl věnovat čas a úsilí naučit se s tímto nástrojem pracovat.
        </p>

        <p>S programováním v tomto nástroji jsem začal ve svých 10ti letech na Vánoce v roce 1999, kdy jsem dostal CDčko s vizuálem, který vidíte
            tady na obrázcích, pod stromeček. Začal jsem tvořit metodou pokus omyl a stejně jako mnozí jiní, si dnes
            nedokážu absolutně představit, jak jsem u toho mohl se svým tehdejším přístupem vydržet. Možná proto se vyplácí
            začít co nejdříve, když jsme ještě děti.</p>

