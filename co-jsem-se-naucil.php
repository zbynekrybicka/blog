        <p>Já jsem tehdy začal neskutečným způsobem hltat internetové stránky <a href="https://jaknaweb.com/" target="_blank">JakNaWeb.com</a> (pozn. nejednalo se o 
            <a href="https://www.jakpsatweb.cz/" target="_blank">JakPsatWeb.cz</a>, ty jsem objevil až později). Bylo tam obrovské množství (alespoň na tehdejší dobu obrovské)
            tutoriálů, jak něco vytvořit v PHP a já jsem to všechno okamžitě zkoušel. Brzy jsem si vytyčil velký projekt, vytvořit něco jako BlueBoard.cz (tehdy na tom webu
            byla možnost vytvářet si interaktivní prvky jako ankety, komentáře, počítadla atd., které si mohl uživatel na svůj web vložit). Pro úplnost, nebyl to úplně
            můj nápad, podnět mi k tomu dal jiný tvůrce, který právě dělal něco podobného a pak jsme se kvůli tomu i trochu pohádali, protože mě (částečně oprávněně) nařkl
            z plagiátorství.
        </p>

        <blockquote>Tehdy jsem si poprvé náležitě uvědomil, co to znamená provozovat on-line aplikaci a jaké noční můry při laxním přístupu k práci způsobují neodhalené bugy
            a celkově řádně neotestovaná aplikace. A to jsem ještě neměl ani tušení, co přijde v budoucnu.
        </blockquote>

        <p>Tento svůj projekt jsem pojmenoval nejprve EagleBoard a když mi začali vyhrožovat, že zneužívám ochrannou známku přejmenoval jsem ho na FalconBoard (podotýkám,
            že to nemá nic společného ani s lodí Millenium Falcon ani s raketami Falcon od SpaceX, jelikož ani jedno jsem v té době neznal, psal se rok 2004). Nejprve jsem
            se pokusil obejít bez MySQL databáze, protože se mi úplně nechtělo se ji učit, tak jsem vše ukládal do souborů, které jsem generoval. Nebyl to ale žádný JSON.
            To je pro slečinky. Já jsem generoval živý PHP kód, který obsahoval hodnoty proměnných, a následně jsem jej pomocí příkazu <em>include</em> vkládal do stránky
            s editačním formulářem a také s výsledným interaktivním elementem. (takovou bezpečnostní díru nevymysleli ani v Microsoftu)
        </p>

        <p>FalconBoard se mi podařilo dotáhnout do MVP a dokonce jeden můj spolužák, který si v té době taky dělal svoje webové stránky, jej použil. Fungovalo to dost bídně
            a já jsem nesmírně žářlil na konkurenci, ke které potom spolužák přešel.
        </p>

        <h3>HTML+CSS</h3>

        <p class="perex">Možná si říkáte, že je trochu zvláštní, že jsem se učil nejdříve PHP a teprve potom HTML. Ono to tak ale v podstatě bylo, protože HTML jsem sice znal, ale v době,
            kdy jsem se začal učit PHP, jsem v něm uměl jenom nadpisy a odkazy a třeba taková validace mi byla naprosto cizí.
        </p>

        <hr>

        <p>Ve škole jsme se začali s několika spolužáky předhánět v tom, kdo má lepší webové stránky a já jsem rozhodně nechtěl zůstat pozadu. Jeden spolužák nás však všechny
            trumfnul způsobem, který jsem dlouho nechápal, dokud mi o pár let později nedošlo, že mě zmátla terminologie. On totiž prohlásil, že pro layout stránek nepoužívá
            tabulky ale pouze CSSka. Tak jsem si říkal. Tehdy jsem si to vyložil, že vůbec nepoužívá HTML elementy ale pouze texty. Když jsem jednou procházel stránky
            <a href="https://www.jakpsatweb.cz/" target="_blank">JakPsatWeb.cz</a>, tak se mi podařilo najít ukázku, jak se používají <em>div</em> a <em>span</em> elementy,
            tak se mi podařilo jakousi takousi replikaci jeho stylu, ale stejně jsem to nechápal. On argumentoval tím, že pokud prohlížeč nepodporuje tabulky, tak se stránka
            strukturovaná pomocí tabulky vůbec nezobrazí. Pokud prohlížeč nepodporuje CSS, tak se na stránce zobrazí jen holý text, což je pořád lepší, protože uživatel
            něco uvidí. No, byl to v té době velký problém, když se nezávisle na sobě ve velkých korporacích vyvíjely tři různé prohlížeče (Internet Explorer, Netscape Navigator
            a Mozilla Firefox, každý se snažil uzdmout si co největší část uživatelské základny a každý zobrazoval identický kód úplně jinak. Já opravdu děkuji Bohu za to, že
            tento problém je už dneska naprosto minoritní a standardní elementy a styly podporují všechny prohlížeče stejně (a to je jich dneska víc). 
        </p>

