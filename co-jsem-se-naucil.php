            byly schopné vytvořit komplexní funkcionalitu, která spolu souvisí a současně zůstat velmi přehledné, což vedlo k tomu, že jsem mohl mít všechno podstatné na jednom místě a změny bylo možné
            provádět velmi snadno.
        </p>

        <h4>A proč tento systém nepoužívám?</h4>

        <p>Ty důvody jsou ve skutečnosti dva. Ten první zjevný byl, že přišel ChatGPT, který v podstatě dokáže tohle všechno lépe, bez potřeby dalšího programování a ještě se v tom zlepšuje. Já sám v Brně
            uprostřed Evropy s jedním notebookem prostě neporazím tým několika desítek nebo i stovek špičkových programátorů v Sillicon Valley.
            A druhý důvod, proč jsem od tohoto systému upustil, byl ten, že jsem si konečně po mnoha letech uvědomil, že je nakonec přece jen nadbytečný. Nějaké generování šablon se určitě hodí, ale to hlavní,
            co jsem postrádal a co jsem se snažil získat tímto systémem, byl přehled nad aplikací. V současné době pracuji tak, že používám program SimpleMind, do kterého si zapisuji názvy komponent do struktury
        entity aplikace, na které se odkazují. Nemám potom problém dotrackovat, když potřebuji něco přidat, změnit nebo opravit.</p>

        <blockquote>Za sebe můžu zcela zodpovědně prohlásit, že přes všechna ta úskalí,
            nebo možná spíš právě díky nim jsem se do dnešních dní dostal na úroveň, kdy se můžu skutečně označit za programátora analytika a nikoli programátora kodéra a této schopnosti analyticky a také
            strategicky uvažovat, si nesmírně cením.
        </blockquote>

        <h2>Herní enginy</h2>

        <a name="gamemaker" class="anchor"></a>
        <h3>Gamemaker</h3>

        <p class="perex">Bylo pro mě docela hořké zjištění, že pro tvorbu her není nezbytný jen programátorský skill ale také skill grafický. I obyčejná textová adventura musí vypadat dobře, jinak má pramalou
            šanci na úspěch.</p>

        <hr>

        <p>Když jsem se v době, kdy jsem byl plně ponořený do tvorby v Petříkovi, začal zajímat i o jiné možnosti tvorby her, velmi rychle mi přišel do ruky Gamemaker. V rychlosti jsem porovnal jeho možnosti
            a přišel mi naprosto primitivní a jednoúčelový, protože se v něm z mého prvního pohledu daly vytvářet jen asi čtyři typy her. Klikačky typu legendárního <em>Capture the Clown</em>, plošinovky, které
            však v Petříkovi byly daleko složitější, což jsem uvítal, potom střílečky v pohledu shora a nějaké další, viděl jsem třeba izometrické bludiště. V tomto jsem se pravda nejen mýlil ale současně tyto
            uvedené typy pokrývají poměrně širokou škálu nejrůznějších her, z nichž spousta z nich mohou být velmi dobré.
        </p>

        <div class="gallery">
            <img src="gallery/co-jsem-se-naucil/GameMaker.jpg" alt="GameMaker" class="thumbnail">
            <img src="gallery/co-jsem-se-naucil/GamemakerUI.png" alt="GameMaker UI" class="thumbnail">
        </div>

        <p>Nějaké pokusy jsem v něm uskutečnil, ale nic z toho nebylo ani na ukázku, natož na reálnou publikaci. Teprve po mnoha letech, v roce 2024 jsem se rozhodl, že dám GameMakeru opět šanci se hrou,
            která bude vycházet z možností, o kterých vím, že v GameMakeru jsou možné a jsou i již vnitřně implementované. Současně s tím jsem byl připravený, že si sám vytvořím grafiku v profesionálním
            nástroji a způsobem, kde mě nikdo nemůže obvinit z plagiátorství ani jiného kopírování. Prostě jsem si ji nakreslil kompletně sám v Kritě (viz níže).
        </p>

        <blockquote>Žádný engine není všelék, i když ho dokonale ovládáte. Ani žádné AI nedokáže splnit příkaz typu: &bdquo;Vytvoř dobrou hru.&ldquo; Dobrou hru musíte vymyslet a naplánovat do posledního detailu.</blockquote>

        <p>Dnes vím, že GameMaker je zkrátka téměř univerzální herní engine určený pro 2D hry. 3D jsem v něm nezkoušel a ani nemám tu potřebu, jelikož pro 3D hry jsou tu jiné daleko propracovanější enginy.
            Představoval však jeden z mých zásadních milníků v kariéře Indian vývojáře, jelikož právě v něm se mi podařilo publikovat svoji první hru, kterou jsem opravdu dotáhl do konce.
        </p>


        <a name="unity" class="anchor"></a>
        <h3>Unity</h3>

        <p class="perex">Přestože Unity nemá poslední dobou dobrou reputaci a hráči i tvůrci na něj ukazují prstem, u mě má nesmazatelnou zásluhu v oblasti tvorby her. Byl to totiž první engine, ve kterém se mi
            podařilo v dospělém věku vytvořit aplikaci, která vypadala skutečně jako hra, přestože jsem ji nedokončil.
        </p>

        <hr>

        <p>Nesl jsem si to s sebou dlouhou řadu let jako nikdy nezahojenou jizvu z mládí. Potom, co mi přestal fungovat Petřík, protože jsem dostal nový počítač a licenční disketa byla vadná, nemohl jsem již
            dál tvořit hry, které jsem chtěl a to mě strašně bolelo. Opravdu jsem potom nesmírně toužil, ale neviděl jsem žádnou alternativu. GameMaker byl sice zdarma, ale ten v té době moje požadavky nesplňoval.
            Dlouho jsem si myslel, že tvorba her je otázkou jednak softwaru za desítky až stovky tisíc a především hardwaru, který bych ani v běžném obchodě nesehnal a rovněž by stál obrovské jmění. Někdy v roce
            2018, kdy vyšlo Kingdom Come Deliverance, jsem viděl nějaké let's playe a zůstal jsem v němém úžasu, jak nádherná hra to je. Nedalo mi to a zkusil jsem tu nejjednodušší věc, co v danou chvíli šla,
            šel jsem na Google a vyhledal jsem tam (tuším) "tvorba počítačových her". Vyjelo mi několik odkazů a pak mi to došlo. Pro tvorbu her se v dnešní době používají enginy. Ve skutečnosti je nesmysl
            používat samotné knihovny OpenGL a jim podobné, jak jsme se to učili ve škole.
        </p>

        <div class="gallery">
            <img src="gallery/co-jsem-se-naucil/unity.png" alt="Unity engine" class="thumbnail">
            <img src="gallery/co-jsem-se-naucil/unity-editor.jpg" alt="Unity editor" class="thumbnail">
        </div>

        <p>Okamžitě jsem šel na Youtube a tam si vyjel nějaké tutoriály na Unity. Hned se mi na něm ale jedna věc nelíbila. V jednom tutoriálu autor předváděl, jak úplně základní herní koncept FPS včetně
            překážek, jak udělat, aby se ruka pistolníka neotáčela přesně s kamerou ale s drobným zpožděním a nakonec, jak udělat něco jako cut scénu. Bylo to takové jednoduché, ale strašně se mi nelíbilo,
            že jsem viděl, že autor používá v podstatě jenom hotové knihovny funkcionalit.
        </p>

        <blockquote>Myslel jsem si, že každá funkcionalita hry je tak strašně složitá, že je potřeba na ni obsáhlá knihovna a současně, že když budu cokoli potřebova udělat třeba jenom trošku jinak,
            tak mám smůlu, protože jakýmkoli zásahem do jakékoli knihovny bych všechno rozbil.</blockquote>

        <p>Chtěl jsem si vyzkoušet alespoň tady tento základ, ale postavil se mi do cesty málo výkonný hardware. Tedy, málo výkonný... ano, měl jsem MacBook pro, který jsem používal na programování webových
            stránek a celkově on-line aplikací. Unity jsem v něm sice rozjel, ale všechno bylo strašně pomalé a sekalo se to. Nebylo to poprvé ani naposledy, kdy jsem těžce litoval, že jsem byl takový
            egoista a &bdquo;strašně nutně&ldquo; jsem potřeboval notebook s jablkem. Už ho nemám a další si rozhodně koupit neplánuji.   
        </p>

        <p>Když jsem si potom pořídil nový počítač, na kterém jsem si konečně mohl zahrát Kingdom Come, u kterého jsem pak strávil zhruba týden téměř nepřetržitě, řekl jsem si, že zkusím i to Unity. Fungovalo
            výborně i plynule a nepadalo. Na základě podobného tutoriálu jsem vytvořil jednoduchou FPC běhačku, kde bylo možné šoupat bednami ale ne ve stylu Sokobana, zcela volně. Když jsem ale chtěl nějakou
            bednu jakoby vzít do ruky, nevěděl jsem, jak to udělám a opět jsem to vzdal, protože jsem si stále myslel, že je to strašně složité a musel bych kvůli tomu provádět obrovskou řadu výpočtů, které by
            nemusely být úplně spolehlivé. V Petříkovi to tak totiž bylo. Pozice kamery, úhel natočení, na základě toho 3D přímka výhledu, dále rozsah, ve kterém se bude sledovat, jestli je v cestě nějaký objekt
            a pokud ano, je třeba to nějak zaregistrovat, aby bylo možné reagovat na kliknutí. Tohle všechno se mi honilo v hlavě, když jsem přemýšlel, jak to udělat. Netušil jsem, že Unity, i když mě to mohlo
            napadnout, už má tohle všechno vyřešené a funguje to spolehlivě.
        </p>

        <p>Pár měsíců jsem v hlavě nosil nápad na hru, která by byla podobná GTAčku s tím, že by to bylo malé město, zamýšlel jsem i, že by to byla část reálného města, které mám velice rád a hráč by tam byl
            v roli běžného občana, který bude mít co do činění s mafií. Jak originální... Čemu jsem ale věnoval zvláštní pozornost, bylo řízení auta. To jsem chtěl udělat jiné, než je klasika. Plyn a brzda
            na tři klávesy, čímž by se ovlivňovala síla sešlápnutí pedálu, volantem by se točilo pomocí myši a pomocí malíčku by se sešlapávala spojka, což by samozřejmě byla mechanika navíc, kterou by bylo potřeba
            zmáknout. A současně by tam byl atribut &bdquo;driving skill&ldquo;, který by určoval, jak plynule postava hráče ty pedály mačká, což by mělo za následek různou plynulost jízdy. K tomuto konceptu jsem
            došel potom, co jsem slyšel Daniela Vávru mluvit o tom, jak detailně navrhoval auta do Mafie. To vše bylo v době, kdy jsem ještě netušil, zda je vůbec pro mě jako pro běžného smrtelníka vůbec možné
            tvořit hry.
        </p>


        <blockquote>Pak jsem si ale jednou řekl, že na to prostě přijdu, i kdyby mě to mělo stát týden googlení. Ve skutečnosti to byly ani ne dva dny.</blockquote>

        <p>Na podzim 2022, kdy jsem opět začal přemýšlet nad tím, jak by se to dalo udělat a strašně mě odrazovala představa, že je všechno strašně náročné a je k tomu potřeba obrovského talentu a specializace,
            jsem si řekl, že těch her je dneska tolik, že to přece nemůžou produkovat jenom AAA studia. Tak jsem se rozhodl, že se pokusím naučit vytvářet hry v Unity tím způsobem, že postahuji všechny
            assety, přečtu si důkladně, co s nimi autoři zamýšleli a dám z toho dohromady něco smysluplného.</p>

        <p>Tak jsem odpoledne přišel z práce a začal jsem vyhledávat, jak se tvoří do Unity postavy, které vypadají jako lidé a jestli existuje nějaký způsob, jak jim dodat animační pohyb, aniž bych musel
            použít <em>Motion capture</em> nebo je animovat ručně pomocí klíčových snímků. Objevil jsem aplikaci Character creator, kterou jsem si i přes její cenu velmi rychle zakoupil. Pro animace jsem
            objevil server <a href="https://www.mixamo.com/" target="_blank">Mixamo.com</a>. Chvíli jsem tápal a stále se mi do hlavy vkrádala myšlenka, že přece tyto dvě aplikace nejsou kompatibilní a stejně nepůjdou propojit.</p>

        <blockquote>V momentě, kdy jsem uviděl, jak mi na obrazovce pochoduje postavička, kterou jsem sám vytvořil, všechny moje obavy vzaly za své a dopamin mi vystřelil do výšky jako snad nikdy
            v životě.</blockquote>

        <p>Viděl jsem, že to vlastně není tak těžké a začal jsem s vytřeštěnýma očima hledat, co dalšího se dá v tom Unity dělat. Postavu jsem rozpohyboval, aby reagovala na WASD klávesy a potom také, aby
            nastoupila do auta, když k němu přijde. Sice to všechno glitchovalo jak o život, ale já jsem věřil tomu, že to už je potřeba jen doladit. Záhy mi však začalo vadit, že je jen málo volně dostupných
            assetů, které by za něco stály, tak jsem si začal tvořit vlastní. Už jsem věděl, jak fungují, tak se z toho stal můj nový koníček.
        </p>

        <p>Začal jsem tvořit hru <em>Kossai</em>, která vycházela z toho mého nápadu popsaného výše. Je paradoxní, že zatímco jsem si užíval tvorbu vlastních assetů, jízdu autem jsem z této hry vyškrtnul, 
            protože jsem si říkal, že na auta je to město moc malé. Nebylo inspirováno žádným existujícím městem, bylo to v podstatě poskládané trochu jako v SimCity. Hru jsem nedokončil, ale její koncept
            mám na stole stále a ten čeká na svoji realizaci, o které věřím, že jednou opravdu nastane.
        </p>

        <a name="unreal" class="anchor"></a>
        <h3>Unreal Engine 5</h3>

        <p class="perex">Musel jsem najít sílu zahodit projekt, na kterém jsem pracoval několik měsíců a začít odznova a lépe, protože jsem narazil na skleněný strop. S příslibem daleko lepší grafiky za cenu
            úplně nového konceptu práce jsem se rozhodl přejít na Unreal Engine 5.
        </p>

        <hr>

        <p>Postavy z Character creatoru se v Unity značně deformovaly, teda především jejich textury. Zkusil jsem si, jestli by se v Unreal Engine 4 vykreslovaly lépe. Výsledek mě potěšil i zklamal zároveň.
            Potěšilo mě, že se vykreslila skutečně pěkně beze ztráty kvality, zklamalo mě, že tohle Unity nedává a že budu muset přejít na Unreal. Dost mě šokovalo, že koncept Unrealu je dost odlišný od Unity.
            Kolegovi, který moji práci průběžně sledoval, jsem řekl: &bdquo;V Unity se věší skripty na objekty, v Unrealu se věší objekty na skripty.&ldquo; To bylo moje shrnutí prvních dojmů z Unrealu.
        </p>

        <div class="gallery">
            <img src="gallery/co-jsem-se-naucil/unreal-engine.png" alt="Unreal engine" class="thumbnail">
            <img src="gallery/co-jsem-se-naucil/unreal-editor.jpg" alt="Unreal engine editor" class="thumbnail">
        </div>

        <p>Můj druhý dojem byl naprosto fenomenální světelné efekty. V editačním režimu jsem pohyboval Sluncem (ještěže mě za to zlatá rybka neposlala do lahve od octa) a nemohl jsem se nabažit těch nádherných
            barev, které dopadaly na budovu, kterou jsem si vymodeloval v Blenderu (viz níže). S obrovským nadšením jsem se vrhl do tvorby hry, u které jsem si nasadil takovou nevyřčenou laťku, že chci, aby
            byla lepší než <em>Hobo: Tough life</em>, což je hra, která se mi velice líbila, ale měla zjevné nedostatky, což je škoda.
        </p>

        <p>Proplétal jsem všelijak ty nesčetné blueprinty a bylo pro mě obrovským uspokojením vidět, když něco skutečně funguje tak, jak jsem zamýšlel. Bohužel se mi nepodařilo nainstalovat si Unreal v modifikaci,
            abych mohl tvořit a používat i samotný kód C++, což jsem chtěl, protože vůči drag&amp;drop konceptu mám dost zásadní výhrady, přestože jsem na něm vyrostl. Používal jsem animace z 
            <a href="https://www.mixamo.com/" target="_blank">Mixamo.com</a> nejen pro chůzi ale také běhání, tancování, jedna moje postava se modlila a jiná zase upadla po ráně pěstí. Dialogy jsem chtěl nejprve
            řešit pomocí titulků, které by se objevovaly nad hlavou postavy. Později jsem ale zjistil, že existují AI nástroje pro konverzi hlasu, jsem proto připravený dát do hry i dabing. A konečně, objevil jsem
            cenově dostupné řešení Motion capture, díky kterému jsem se do hry rozhodl dát i cut scény.
        </p>

        <blockquote>S tímto vybavením a znalostmi jsem zjistil, že mi skutečně nic nebrání, vyrobit všechny hry, které mám v plánu a jejichž nápady si již dokonce řadu let hýčkám v mysli a v srdci. Jediné, co k tomu
            potřebuji a co mi nikdo jen tak nedá, je čas. Věřím ale, že i toho se mi dostane a nakonec vše zvládnu.
        </blockquote>

        <p>Přesto jsem se však v létě 2024 rozhodl vyzkoušet ještě jednu zásadní změnu. Během jednoho rozhovoru s Danielem Vávrou padlo, že Unreal sice vykresluje nádherné scenerie, které jsou ale pro hru
            nepoužitelné, protože požadují extrémní výpočetní výkon. Naopak vyzdvihl <a href="https://www.cryengine.com/" target="_blank">CRY engine</a> a jeho zásadní výhodu oproti Unrealu, že zvládá daleko
            lépe složitou a detailní 3D grafiku za cenu složitější implementace a vzdání se některých věcí, které jsou v Unrealu samozřejmostí, například LOD. Já jsem se však rozhodl tuto možnost vyzkoušet.
            Zatím jsem neuskutečnil...
        </p>

        <h2>Grafika</h2>

        <a name="blender" class="anchor"></a>
        <h3>Blender</h3>

        <p class="perex">Už v době, kdy jsem programoval v Petříkovi, jsem chtěl dělat 3D grafiku. Nesmírně mě okouzlily hry 13 duchů a Turbo Cars od Miroslava Němečka. Tehdy jsem však nejen, že neuměl udělat
            nic pořádně, ale neměl jsem ani k dispozici nějaký rozumný software.
        </p>

        <p>V roce 2004 mi jeden spolužák přihrál na DVDčku dva pro mě zcela zásadní programy. Adobe After Effects a 3D studio Max. V obou jsem se začal velmi intenzivně učit pracovat a k mému údivu mi obojí 
            šlo na tu dobu překvapivě dobře. Tyto dva programy plus film Star Wars Epizoda 3 - Nebezpečně nebezpečné nebezpečí, což byla unikátní parodie, která byla vydána ještě předtím než vyšla premiéra 
            originálu, pro mě byly podněty k tomu, že jsem začal filmovat. Programovat hry jsem v té době nemohl, protože Petříka jsem již neměl a jiné způsoby tvorby her jsem neznal. Videa mi ale začala
            docela jít. Natočil jsem několik filmů, jejichž úroveň byla dílo od díla lepší, přestože dnes bych se pod ně nejspíše nepodepsal. Určité střípky z těchto videí jsou stále aktuální.
            Tato videa dodnes naleznete na <a href="https://vimeo.com/user874970" target="_blank">Vimeo.com</a>.
        </p>

        <p>Koupil jsem si, nebo spíš moje máma mi tehdy koupila Berušky II od Anakreonu, o kterých jsem se tady už zmiňoval. Tato hra byla skvělá, až na jednu věc, byla zabugovaná.
            Já jsem si vyměnil pár e-mailů s tvůrci. Příčinu chyby se nám zjistit nepodařilo, ale jen ze zvědavosti jsem se zeptal, v čem tu hru tvořili. Odpověď byla Microsoft Visual C++ a Blender.
            Chtěl jsem si Blender vyzkoušet, protože byl (a dodnes je) zcela zdarma a tak skvělá hra jako Berušky, pokud ji udělali v Blenderu, tak bych neměl zůstat pozadu.
            Dostal jsem však těžkou ránu mezi oči. Připadal jsem si jako kdyby letitému tvůrci ve Photoshopu předhodili GIMP. Zkoušel jsem, co se v tom dá dělat. Něco málo se mi i podařilo, ale pořád to 
            bylo těžce pod úrovní toho, co jsem dokázal v 3Ds MAX. Nevím, zda to bylo možné už tehdy, ale jednu věc jsem udělal opravdu špatně, že jsem si více nepozjišťoval, co a jak v Blenderu opravdu funguje.
            Třeba jsem byl naprosto zoufalý v tom, že jsou na kouli vidět polygony, přestože v 3D studiu se ta koule jevila jako dokonale hladká, přestože těch polygonů nebylo potřeba zdaleka tolik.
            Dnes už vím, že to byla otázka jednoho přepínače, pouze nevím, zda tam byl už tehdy nebo ho doplnili až později.
        </p>

        <blockquote>Říkal jsem si, že to prostě nemůže být tak neprůstřelné, jak se to v tu chvíli zdálo, tak jsem si zaplatil kurz na 
            <a href="https://www.udemy.com/share/101X0w3@Hqfpj8Fz140DY3r5YX34vw_JVFdVi1_eM8lGP-56aCm9PaK7FIV6P1hwCBWDt3u-/" target="_blank">Udemy.com</a>.
        </p>
        
        <p>Tento kurz nebyl vůbec špatný a asi nebude od věci, když si ho znovu projdu, protože autor je rozhodně profíkem. Bohužel se mi však ani po tomto kurzu nepodařilo práci v Blenderu uchopit na 
            použitelné úrovni. Dnes už vím, že důvodem nebyly chybějící skills ale chybějící plán postupu a nápady.
        </p>

        <p>V době, kdy jsem začínal aktivně tvořit v Unity a kdy jsem psal, jak mi dopamin vystřelil jako nikdy v životě, jsem si uvědomil, že tvorba assetů, především 3D modelů se v tuto chvíli jeví jako zásadní. 
            Rozhodl jsem se proto dát Blenderu ještě šanci. K mému překvapení jsem objevil video, které můj pohled na něj zcela změnilo. V tom videu bylo ukázáno, jak tvořit v Blenderu domečky skoro tak jednoduše 
            jako v The Sims. Prostě táhnout zdi pomocí Extrude nejlépe po jednom metru. Já jsem to tak udělal a ve svojí prozíravosti jsem udělal navíc to, že pokud nějaká zeď měla končit v rohu, udělal jsem 
            vytažení pouze o 90 cm a pak ještě navíc o 10 cm, přičemž následující vytažení bylo v pravém úhlu. Můj první domeček byl samozřejmě úplně strašný. Zdi byly tři metry vysoké, což by nevadilo, ale 
            vchod neměl horní rám a když jsem chtěl přidat patro, vůbec jsem nemyslel na to, kde by měly být schody. Uvědomil jsem si však, že záleží skutečně na každém polygonu, což se mi s přibývající praxí 
            stále znovu a znovu potvrzuje, přestože se pohybuji již v tisících až desítkách tisíc polygonů.
        </p>

	<!--Nafotil jsem si Ikeu-->
 
        <p>Vymodeloval jsem si jeden malý domeček s jedním obývacím pokojem a koupelnou. Ten jsem potom v Unity nakopíroval asi padesátkrát do dvou čtvrtí, ve kterých se měla odehrávat moje hra. Přemýšlel jsem, 
            jak ty domečky vybavím. Stahovat nábytek z internetu se mi nechtělo, tak jsem si zajel do Ikea Showroomu v Brně, kde jsem si nafotil každou židličku, každý stůl i každý stojánek na tužky, který se
            mi nějak zalíbil s tím, že si je vymodeluji. Několik týdnů následně se mi dařilo vymodelovat třeba tři až čtyři kusy nábytku denně, na což jsem byl náležitě hrdý a strašně mě to bavilo. Potom ale 
            nastal obrovský šok. Ten nábytek se do toho domu nevešel, protože měl špatně navržené rozměry, které nebyly kompatibilní. A násilným škálováním bych ztratil proporce, které jsem měl připravené pro 
            užívání nábytku lidmi.
        </p>

        <blockquote>Byl jsem z toho v šoku, ale nechtěl jsem to vzdát, tak jsem si řekl, že se teda pokusím vymodelovat si nějaké jiné domy, které již budou rozměrově odpovídat. Nechtělo se mi ale dělat jen tak 
            nějaké. Najednou se mě zmocnila potřeba vytvářet krásné věci.</blockquote>

	<!--Jel jsem do Jihlavy-->
    
        <p>Několik dní jsem spekuloval nad tím, co bych tak mohl zkusit a procházel jsem i různá města v ČR přes street view. A pak jsem na to kápnul. Zajedu si do Jihlavy, tam jsou moc pěkné domečky, 
            které si nafotím a vymodeluji. Už ani nevím, jak mě to přesně napadlo, protože do té doby jsem tam znal je Bránu matky Boží, ale možná jsem chtěl taky udělat jakousi nepsanou radost Dušanovi Majerovi, 
            kterého již léta sleduji na <a href="https://www.stream.cz/dobyvanivesmiru" target="_blank">Stream.cz</a> i dnes na <a href="https://fameplay.tv/vesmirne-zpravy">FamePlay.tv</a>.</p>
				
        <p>Do Jihlavy jsem se vydal. Ten den bylo zataženo a trochu i mlha, ale doufal jsem, že můj celkový záměr to nezkazí. Když jsem vystoupil z auta na Náměstí T.G. Masaryka. myslel jsem, že mi upadnou 
            prsty na rukou, jaká byla zima, se kterou jsem nepočítal. To se naštěstí po nějaké chvíli srovnalo a já jsem začal fotit domy v Benešově ulici. Ani jsem do té doby netušil, že Muzeum Vysočiny Jihlava
            je právě tam. Nafotil jsem si asi 20 domečků, které jsem pak následně začal modelovat. Hodně jsem se ale zamýšlel nad otázkou, jaký je vlastně rozdíl mezi gotikou a renesancí. Trošku jsem si zapátral 
            na internetu a nalezl jsem z těch několika zdrojů stručnou odpověď: Gotika je zaměřená na Boha, renesance je zaměřená na člověka. A tehdy jsem si řekl: &ldquo;Právě tento vnitřní konflikt ve mně
            nyní probíhá. To je neuvěřitelně příhodné.&bdquo; Skutečnost je taková, že asi ne kvůli tomuto výletu, ale díky této otázce jsem se právě v té době dal na cestu víry a stal se křesťanem. Na tohle 
            téma bych také dokázal mluvit hodiny a hodiny, ale to není předmětem tohoto už tak dlouhého článku. Když jsem se vrátil zpátky domů, začal jsem opět modelovat. První, co jsem si vzal na paškál, 
            byl úřad, který můžete vidět na jednom z těchto obrázků. Schválně jej srovnejte s originálem. Jak jsem byl zvyklý vytvářet tři modely denně, tento mi trval zhruba tři dny a začínal jsem již být 
            netrpělivý, že to trvá strašně dlouho, ale rozhodně toho nelituji. Výsledek mě naprosto ohromil. A všechny další domečky jsem chtěl dělat tímto stylem, na čemž trvám dodneška.
        </p>

    <!--Koupil jsem si odměřovací kolečko a jel jsem do Blanska nafotit si Dřevěný kostelík.-->

        <p>S ohledem na to, že jsem se dal na cestu víry, byl pro mě zcela přirozený krok zakomponovat ji i do her. Využil jsem toho, že se považuji za člověka, který se dokáže hodně vžít do myšlenek a pocitů 
            druhých lidí a taky jsem věděl, jaký byl můj život víry předtím, téměř bych řekl křižácký. Zcela logický následující krok byl umístit do hry nějaký kostel. Přemýšlel jsem, jaký bych tam mohl dát
            a napadl mě jeden, se kterým mám výjimečnou zkušenost. Dřevěný kostelík v Blansku. Tam se vdávala moje sestra a takovou svatbu jsem snad nikdy předtím nezažil. Velmi jsme se u toho bavili a se 
            sympatickým panem farářem byla velká legrace. Tak jsem se prostě a jednoduše vydal jeden den do Blanska, abych si kostelík nafotil a taky změřil. K tomu jsem si pořídil odměřovací kolečko.
            Výsledek posuďte sami. Modelování mi trvalo zhruba tři dny. A náměstí, na které jsem ve hře umístil kostelík, jsem pojmenoval po onom faráři, který oddával moji sestru. Bohužel, tento design 
            dokument jsem nakonec skartoval a tato hra bude úplně jiná. Tento kostelík tam však bude, jen na úplně jiném místě.
        </p>

    <!--Nafotil jsem si radnici v Ostravě-->

        <p>Jednou jsem se takto vydal na výlet s tím, že si prostě odpočinu. Jel jsem do Ostravy. Chtěl jsem si prohlédnout důl Petr Bezruč, ten, který má nad krásnou kovanou branou nápis 
            <em>Petr, něco červenýho, Bezruč</em>. Důl jsem viděl, ale po cestě jsem natrefil na něco, co mě doslova okouzlilo. Slezskoostravská radnice, která byla v mých očích doslova a do písmene uměleckým 
            dílem. Jakmile jsem ji viděl, okamžitě jsem věděl, že takovou stavbu chci do hry také. Udělal jsem si pár fotek a co jsem nezachytil, s tím mi pomohl Google maps a Google Street View. Výsledek můžete 
            vidět na obrázku. Jen není dokončená a pro finální verzi hry ji budu chtít vybavit i zevnitř.
        </p>

    <!--Zajel jsem si do Telče-->

        <p>A konečně, navštívil jsem Náměstí Zachariáše z Hradce v Telči, kompletně jsem si jej nafotil a změřil a začal jsem stavět domečky z něj. Část, kterou jsem zatím udělal, můžete rovněž vidět na obrázcích.</p>

