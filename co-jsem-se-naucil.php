        <a name="petr" class="anchor"></a>
        
        <h3> Petr &ndash; Gemtree (Pokud bych dnes začínal, daleko více a důsledněji bych plánoval)</h3>

        <p class="perex">Každý nějak začínal a každý se musel od něčeho odpíchnout. Já jsem začínal na vizuálním programovacím nástroji 
            <a href="http://www.gemtree.cz" target="_blank">Petr</a> od společnosti Gemtree.</p>

        <hr>

        <p>Pokud jste v programování naprostí zelenáči, ale vidíte v něm určitý hlubší smysl, rozhodně se vyplatí dát tomuto
            vizuálnímu nástroji šanci. Přestože se čistě jen s ním asi nedostanete mezi programátorskou elitu, rozhodně má
            smysl věnovat čas a úsilí naučit se s tímto nástrojem pracovat.
        </p>

        <p>S programováním v tomto nástroji jsem začal ve svých 10ti letech na Vánoce v roce 1999, kdy jsem dostal CDčko s vizuálem, který vidíte
            tady na obrázcích, pod stromeček. Začal jsem tvořit metodou pokus omyl a stejně jako mnozí jiní, si dnes
            nedokážu absolutně představit, jak jsem u toho mohl se svým tehdejším přístupem vydržet. Možná proto se vyplácí
            začít co nejdříve, když jsme ještě děti.</p>

        <div class="gallery">
            <img src="gallery/co-jsem-se-naucil/petrik-obal.jpg" alt="Petřík - obal" class="thumbnail">
            <img src="gallery/co-jsem-se-naucil/petrik-editor.png" alt="Petřík - editor" class="thumbnail">
        </div>



        <p>Většinu tajů o programování, které jsem se v Petříkovi naučil, jsem objevil sám, ale tu úplně základní podstatu mi naštěstí vysvětlil
            můj táta, který prý kvůli tomu musel dost změnit alespoň dočasně způsob nahlížení na programování, jelikož on začínal s Assemblerem
            a svého nejvyššího skillu dosáhl v Pascalu. Vysvětlil mi, jak fungují větvicí příkazy (ve většině programovacích jazyků známé jako
            <em>if</em> a <em>while</em>) a taky, jak funguje přiřazování hodnot do proměnných a výrazy. Pokud bych to vzal hodně zjednodušeně,
            toto bylo vlastně všechno, co jsem potřeboval vědět a všechno ostatní jsem mohl vyzkoumat v tom nepřeberném množství dostupných příkazů
            jak pro řízení programu, tak i řízení sprajtů, vykreslování grafiky a později i 3D grafiky.
        </p>

        <p>Pokusů vytvořit nějaké hry jsem absolvoval spoustu. Bohužel, vyšel mi jen jeden, kdy se mi podařilo udělat něco, co by se už při troše
            dobré vůle dalo nazvat hrou. Ta hra se jmenovala Friends, bohužel, přestože si úplně přesně pamatuji téměř každý detail z ní, nemám ji
            již k dispozici, protože skončila na některém z mých bývalých počítačů v některém ekodvoře. Pokud byste si chtěli udělat představu o tom,
            jak ta hra asi vypadala, tak se můžete podívat na Berušky od <a href="https://anakreon.cz/" target="_blank">Anakreonu</a> a představit si
            jakýsi hybrid mezi jedničkou a dvojkou. Byla ve 3D, ale byla jen na jedné vrstvě a měla v podstatě všechny logické prvky z jedničky kromě
            barevných klíčů.
        </p>

        <blockquote>Uvědomoval jsem si, že samotná znalost programování nestačí. Je třeba mít také cit pro grafiku a vidět, co je skryté.</blockquote>

        <p>Pokud bych dneska s tímto programovacím nástrojem znovu začínal a věděl, co vím nyní, hlavní rozdíl v přístupu, který bych měl, že bych si
            všechno daleko důsledněji plánoval, což by se projevilo především tak, že bych pravděpodobně tvořil daleko menší bloky kódu a snažil se mít
            jednotný standard (zásadně neříkám coding-standard, protože tady se o něco takového bohatě postaral vizuál) v nazývání proměnných a funkcí
            a také v předávání dat, protože můj největší kámen úrazu byl především v tom, že jsem dělal všechno bez řádného zamyšlení a přestože jsem si 
            tam často natahal spousty příkazů, které třeba i fungovaly, tak jsem často ztratil niť a třeba druhý den jsem už nevěděl, co jsem dělal včera
            a absolutně se mi nechtělo pokračovat v něčem, co mi v ten okamžik přestalo dávat smysl. To se mi už naštěstí po těch letech podařilo náležitě
            zmapovat a vyvinout si techniky, díky kterým tento problém ustoupil na pozadí a už na něj téměř nenarážím.
        </p>

        <a name="php" class="anchor"></a>

        <h3>PHP</h3>

        <p class="perex">Slýchal jsem to často a odmítal jsem si to přiznat, že vizuální drag&amp;drop programování není zrovna zářná budoucnost, zvlášť, pokud bych se
            chtěl programováním živit. Výjimečně můžu říct, že mi v něčem velmi užitečném do života pomohla škola.
        </p>

        <hr>

        <p>V deváté třídě na základní škole jsme se začali učit základy PHP a já jsem konečně opustil právě onu vizuální klikačku a začal jsem se s obrovským
            nadšením věnovat tvorbě dynamických webových stránek. Dokonce to bylo ještě předtím, než jsem vůbec tušil, že existuje nějaký free webhosting.
            Pořád jsem poslouchal, jak je provozování internetových stránek velice drahé (ona i pětistovka měsíčně pro mě tehdy byla téměř likvidační). Každopádně,
            náš pan učitel, který jako jediný z celého akademického personálu zřejmě věděl o počítačích i něco více než jen, jak se otevírá Word a Excel a jeho
            znalosti nebyly omezené na osnovu, kterou by si přečetl před hodinou, mě naučil základní princip, jak vlastně PHP funguje a že se tedy jedná o preprocesor,
            který upravuje zdrojový kód, co se posílá klientovi, když si chce zobrazit stránku. Proto tedy dynamické stránky.
        </p>

        <div class="gallery">
            <img src="gallery/co-jsem-se-naucil/php.png" alt="PHP slon" class="thumbnail">
            <img src="gallery/co-jsem-se-naucil/php-hovnokod.png" alt="PHP - hovnokod.cz" class="thumbnail">
        </div>

        <p>Já jsem tehdy začal neskutečným způsobem hltat internetové stránky <a href="https://jaknaweb.com/" target="_blank">JakNaWeb.com</a> (pozn. nejednalo se o 
            <a href="https://www.jakpsatweb.cz/" target="_blank">JakPsatWeb.cz</a>, ty jsem objevil až později). Bylo tam obrovské množství (alespoň na tehdejší dobu obrovské)
            tutoriálů, jak něco vytvořit v PHP a já jsem to všechno okamžitě zkoušel. Brzy jsem si vytyčil velký projekt, vytvořit něco jako BlueBoard.cz (tehdy na tom webu
            byla možnost vytvářet si interaktivní prvky jako ankety, komentáře, počítadla atd., které si mohl uživatel na svůj web vložit). Pro úplnost, nebyl to úplně
            můj nápad, podnět mi k tomu dal jiný tvůrce, který právě dělal něco podobného a pak jsme se kvůli tomu i trochu pohádali, protože mě (částečně oprávněně) nařkl
            z plagiátorství.
        </p>

        <blockquote>Tehdy jsem si poprvé náležitě uvědomil, co to znamená provozovat on-line aplikaci a jaké noční můry při laxním přístupu k práci způsobují neodhalené bugy
            a celkově řádně neotestovaná aplikace. A to jsem ještě neměl ani tušení, co přijde v budoucnu.
        </blockquote>

        <p>Tento svůj projekt jsem pojmenoval nejprve EagleBoard a když mi začali vyhrožovat, že zneužívám ochrannou známku přejmenoval jsem ho na FalconBoard (podotýkám,
            že to nemá nic společného ani s lodí Millenium Falcon ani s raketami Falcon od SpaceX, jelikož ani jedno jsem v té době neznal, psal se rok 2004). Nejprve jsem
            se pokusil obejít bez MySQL databáze, protože se mi úplně nechtělo se ji učit, tak jsem vše ukládal do souborů, které jsem generoval. Nebyl to ale žádný JSON.
            To je pro slečinky. Já jsem generoval živý PHP kód, který obsahoval hodnoty proměnných, a následně jsem jej pomocí příkazu <em>include</em> vkládal do stránky
            s editačním formulářem a také s výsledným interaktivním elementem. (takovou bezpečnostní díru nevymysleli ani v Microsoftu)
        </p>

        <p>FalconBoard se mi podařilo dotáhnout do MVP a dokonce jeden můj spolužák, který si v té době taky dělal svoje webové stránky, jej použil. Fungovalo to dost bídně
            a já jsem nesmírně žářlil na konkurenci, ke které potom spolužák přešel.
        </p>

